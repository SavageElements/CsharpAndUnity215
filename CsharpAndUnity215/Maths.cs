using System;
using System.Collections.Generic;
using System.Text;

namespace CsharpAndUnity215
{
    public class  MathMethods
    {
        public static int DoubleUP (int X)
        {
            int M1 = X;
            int dubUP = M1 * 2;
            return dubUP;
        }

        public static int PITime(int X)
        {
            int M2 = X;
            int piTime = M2 * 3;
            return piTime;
        }

        public static int AddSeven(int X)
        {
            int M3 = X;
            int plusSEV = M3 + 7;
            return plusSEV;
        }
    }
}
